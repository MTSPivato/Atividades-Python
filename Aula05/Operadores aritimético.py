"""Operadores aritimético

+, -, *, /, //, **, %, ()
"""

print('Soma "10 + 10": ', 10 + 10)  # Soma
print('Subtração "10 - 2": ', 10 - 2)  # Subtração
print('Multiplicação "10 * 10": ', 10 * 10)  # Multiplicação
print('Multiplicação "7 ** 7": ', 7 ** 7)  # Exponenciação
print('Divisão "10 / 2": ', 10 / 2)  # Divisão
print('Divisão inteira "10 // 3": ', 10 // 3)  # Divisão inteira
print('Resto da divisão "10 % 3": ', 10 % 3)  # Modulo ou resto da divisão

print((9 + 5) / 3)
print('20'+'l')

